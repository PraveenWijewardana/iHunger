    public void placeOrder(){
        L1:do{
            String newCustomerId = customerId.getText().replace("-", "");
            //validate qty
            if (qty.getText()==null || qty.getText().isEmpty() || Integer.parseInt(qty.getText())<1) {
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_CENTER, "Invalid contity..!");
                break L1;
            }else{
                Order order = new Order(labelorderId.getText(), newCustomerId, Integer.parseInt(qty.getText()));
                Order temp[] = new Order[Home.orderArray.length+1];
                for (int i = 0; i < Home.orderArray.length; i++) {
                    temp[i]=Home.orderArray[i];
                }
                temp[temp.length-1]=order;
                Home.orderArray=temp;
                
                for (int i = 0; i < Home.customerArray.length; i++) {
                    if(Home.customerArray[i].getCustomerId().equals(newCustomerId)){
                        Home.customerArray[i].setTotal(Integer.parseInt(qty.getText())*500);
                        break;
                    }
                }
                
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_CENTER, "Order Placed..!");
                setCustomerId();
                clear();
                customerId.requestFocus();
                break;
            }
   
        }while(true);
    }

