    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        L1:do{
            String newCustomerId = customerId.getText().replace("-", "");
            boolean ava=false;
            if(newCustomerId.length()!=10 | newCustomerId.charAt(0)!='0') {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_CENTER, "Invalid Customer Id..!");
            break L1;
            }
            customerId.setEditable(false);
            for (int i = 0; i < Home.customerArray.length; i++) {
                if(newCustomerId.equals(Home.customerArray[i].getCustomerId())){
                    ava=true;
                    name=Home.customerArray[i].getName();
                    break;
                }
            }
            if(ava==true){
                jButton2.setEnabled(false);
                btnPlaceOrder.setEnabled(true);
                cusName.setEditable(false);
                cusName.setText(name);
                qty.requestFocus();
            }else{
                jButton2.setEnabled(false);
                cusName.setEditable(true);
                cusName.requestFocus();
                addCustomer.setEnabled(true);
            }
            break;
        }while(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void addCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerActionPerformed
        L1:do{
            String newCustomerId = customerId.getText().replace("-", "");
            if(cusName.getText().equals("")){
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_CENTER, "Enter customer name..!");

                break;
            }
            Customer customer = new Customer(newCustomerId, cusName.getText());
            Customer temp[] = new Customer[Home.customerArray.length+1];
            for (int i = 0; i < Home.customerArray.length; i++) {
                temp[i]=Home.customerArray[i];
            }
            temp[temp.length-1]=customer;
            Home.customerArray=temp;
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_CENTER, "Customer added..!");
            addCustomer.setEnabled(false);
            cusName.setEditable(false);
            customerId.setEditable(false);
            btnPlaceOrder.setEnabled(true);
            qty.requestFocus();
            break;
        }while(true);
            
            
    }//GEN-LAST:event_addCustomerActionPerformed

    private void qtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qtyKeyReleased
        String qtyText = qty.getText().trim(); // Get text and remove leading/trailing whitespace
        int quantity = 0;
        if (!qtyText.isEmpty()) {
            try {
                quantity = Integer.parseInt(qtyText);
            } catch (NumberFormatException e) {
                quantity = 0; 
            }
        }

        // Update the Net Total
        labelNetTotal.setText("Rs. "+(quantity * 500));
    }//GEN-LAST:event_qtyKeyReleased

