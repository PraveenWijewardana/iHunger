    private void btnUpdateOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateOrderActionPerformed
        L1:do{
            if (qty.getText()==null || qty.getText().isEmpty() || Integer.parseInt(qty.getText())<1) {
                Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_CENTER, "Invalid contity..!");
                break L1;
            }else{
                int index=-1;
                for (int i = 0; i < Home.orderArray.length; i++) {
                    if (orderId.getText().equalsIgnoreCase(Home.orderArray[i].getId())) {
                        index=i;
                        break;
                    }
                }
                Home.orderArray[index].setPrice(Integer.parseInt(qty.getText()));
                String status="";
                if (comboStatus.getSelectedIndex()==0) {
                    status= Order.STATUS1;
                }else if (comboStatus.getSelectedIndex()==1) {
                    status= Order.STATUS2;
                }else if (comboStatus.getSelectedIndex()==2) {
                    status= Order.STATUS3;
                }
                Home.orderArray[index].setStatus(status);
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_CENTER, "Order Updated..!");
                btnUpdateOrder.setEnabled(false);
                labelNetTotal.setText("Rs. 000.00");
                qty.setText("");
                comboStatus.setEnabled(false);
                comboStatus.setSelectedIndex(0);
                orderId.setText("B");
                break;
            }

        }while(true);
    }//GEN-LAST:event_btnUpdateOrderActionPerformed

