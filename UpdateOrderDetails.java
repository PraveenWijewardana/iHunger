    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index=-1;
        for (int i = 0; i < Home.orderArray.length; i++) {
            if (orderId.getText().equalsIgnoreCase(Home.orderArray[i].getId())) {
                index=i;
                break;
            }
        }
        if (index!=-1) {
            if (Home.orderArray[index].getStatus().equals("Prepairing")) {
                comboStatus.setEnabled(true);
                qty.setEnabled(true);
            }else{
                btnUpdateOrder.setEnabled(false);
                qty.setEnabled(false);
            }
            qty.setText(Home.orderArray[index].getPrice()+"");
            switch (Home.orderArray[index].getStatus()) {
                case "Prepairing":
                    comboStatus.setSelectedIndex(0);
                    break;
                case "Delevered":
                    comboStatus.setSelectedIndex(1);
                    break;
                case "Canceled":
                    comboStatus.setSelectedIndex(2);
                    break;
            }
            
            labelNetTotal.setText("Rs. "+(Home.orderArray[index].getPrice()*500));
            btnUpdateOrder.setEnabled(true);
            qty.requestFocus();
            qty.setEditable(true);
        }else{
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_CENTER, "Invalid Order Id..!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

